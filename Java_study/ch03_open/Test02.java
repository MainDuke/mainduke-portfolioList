//관계연산
//> , < , >= , <= , == , !=
// =>, =<  이런 연산자는 사용 불가.

class Test02
{
public static void main( String [] args )
{
	System.out.println(5>3); //true
	System.out.println(5<3); //true
	System.out.println(5>=3); //true
	System.out.println(5<=3); //true
	System.out.println(5==3); //true
	System.out.println(5!=3); //true
		
	
	
}// end main
}